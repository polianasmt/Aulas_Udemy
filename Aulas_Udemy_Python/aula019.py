"""
introdução ao desempacotamento 
é basicamente retirar os itens de uma lista, de um pacote de valores
se queremos tirar apenas um valor do pacote,
utilizamos "_" o underline pra referenciar que vai ser
apenas uma váriavel de uso único, que ela nunca mais 
vai ser usada dentro do programa.

"""

_, _, nome = ['Poliana', 'Henrique', 'Titiane']

print(nome)

